def penjumlahan(a, b) :
    return a + b

result = penjumlahan(4, 5)
print(result)

def pengurangan(a, b) :
    return a - b

result = pengurangan(5, 3)
print(result)

def perkalian(a, b) :
    return a * b

result = perkalian(3, 5)
print(result)

def pembagian(a, b) :
    return a / b

result = pembagian(9, 3)
print(result)


def modulus(a, b) :
    return a % b

result = modulus(5, 2)
