# 1. Statement sederhana
print("Hello, World!") #bisa menggunakan petik dua
print('Hello, World!') #bisa menggunakan petik satu

# 2. Indentasi sangat penting di Python
if 3 > 2:
    print("Tiga lebih besar dari dua")

# 3. Variabel sederhana
x = 3
y = "Python"
print(x)
print(y)

# 4. Multi-line statement menggunakan backslash
a = 10 + 20 + \
    30 + 40
print(a)

# 5. String multi-line dengan triple quotes
text = """Ini adalah string
yang terdiri dari
beberapa baris"""
print(text)