
# operators.py menunjukkan penggunaan operator dasar di python

a = 10
b = 3
# Penjumlahan
print("a + b =", a + b)
# Pengurangan
print("a - b =", a - b)
# Perkalian
print("a * b =", a * b)
# Pembagian
print("a / b =", a / b)
# Modulus
print("a % b =", a % b)
# Eksponen
print("a ** b =", a ** b)
# Pembagian lantai
print("a // b =", a // b)

# Operator logika
x = True
y = False
print("x and y =", x and y)
print("x or y =", x or y)
print("not x =", not x)

# Operator perbandingan
print("a > b =", a > b)
print("a < b =", a < b)
print("a == b =", a == b)
print("a != b =", a != b)
print("a >= b =", a >= b)
print("a <= b =", a <= b)